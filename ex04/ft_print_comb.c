void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb(void)
{
	char a;
	char b;
	char d;

	a = '0';
	b = '0';
	d = '0';

	while(a <= '9')
	{
		while(b <= '9')
		{
			while(d <= '9')
			{
				ft_putchar(a, b, d);
				d++;
			}
			b++;
		}
		a++;
	}
}
